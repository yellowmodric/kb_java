package ch02.sec13;

public class CharExample {
    public static void main(String[] args) {
        char c1 = 'A'; //문자 저장
        char c2 = 65;

        char c3 = '가';
        char c4 = 44032; //숫자를 대입하면 숫자에 해당하는 문자로 변환

        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);
        System.out.println(c4);
    }
}
